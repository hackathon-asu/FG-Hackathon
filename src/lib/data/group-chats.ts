/**
 * Pre-seeded group chat messages for community groups.
 * ~100 messages per group with field-relevant conversation.
 */

export interface GroupMessage {
  id: string
  sender: string
  text: string
  /** Minutes ago from "now" */
  minutesAgo: number
}

const names = [
  'Alex', 'Jordan', 'Priya', 'Carlos', 'Aisha', 'Mei', 'Devon', 'Fatima',
  'Liam', 'Sophia', 'Jamal', 'Rosa', 'Tyler', 'Nina', 'Kenji', 'Maria',
  'Deshawn', 'Linh', 'Omar', 'Camila', 'Raj', 'Aaliyah', 'Tomás', 'Hana',
]

function pick<T>(arr: T[]): T { return arr[Math.floor(Math.random() * arr.length)] }
function sender(): string { return pick(names) }

function buildMessages(texts: string[]): GroupMessage[] {
  return texts.map((text, i) => ({
    id: `gm-${i}`,
    sender: sender(),
    text,
    minutesAgo: (texts.length - i) * 3, // every ~3 min
  }))
}

